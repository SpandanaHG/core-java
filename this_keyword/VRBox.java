class VRBox {

    String material;
    int height;
    double weight;
    String color;
    boolean isWooden;
    String brand;
    int price;
    String usage;
    String country;
    String quality;

    VRBox(String material, int height, double weight, String color, boolean isWooden, String brand, int price, String usage, String country, String quality) {

        this.material = material;
        this.height = height;
        this.weight = weight;
        this.color = color;
        this.isWooden = isWooden;
        this.brand = brand;
        this.price = price;
        this.usage = usage;
        this.country = country;
        this.quality = quality;
    }
}