class StopWatch {

    void start() 
	{
		System.out.println("Start timing"); 
	}
	
    void stop() 
	{
		System.out.println("Stop timing"); 
	}
	
    void reset() 
	{
		System.out.println("Reset time"); 
	}
	
    void display() 
	{
		System.out.println("Show time"); 
	}
	
    void accuracy() 
	{
		System.out.println("Basic accuracy"); 
	}
}

